def format_glossary_for_prompt() -> str:
    """返回格式化的术语对照文本，可直接注入 LLM system prompt。"""
    lines = []

    def _section(title, mapping):
        lines.append(f"\n## {title}")
        # Deduplicate while preserving order
        seen = set()
        for jp, cn in mapping.items():
            if (jp, cn) not in seen:
                lines.append(f"  {jp} → {cn}")
                seen.add((jp, cn))

    _section("棋子名称", PIECES)
    _section("游戏术语", CONCEPTS)
    _section("开局与战型", OPENINGS)
    _section("防守阵型", CASTLES)
    _section("常用短语", PHRASES)

    lines.append("""
## 翻译规则（务必遵守）
1. 棋谱记录中的▲转换为☗（先手/黑方），△转换为☖（后手/白方）
2. 棋步格式统一为：☗/☖ + 数字 + 段位 + 棋子名
   示例：▲７六歩 → ☗7六步    △３四歩 → ☖3四步
   示例：▲２六飛 → ☗2六飞    △８八角成 → ☖8八角成
3. 同的场合写「同」，格式如：☖同歩
4. 棋谱中的数字保留全角/半角原样（７ vs 7）
5. 说明性文本中的汉字数字可视情况译为阿拉伯数字
6. 忽略原文排版换行，按自然段落输出中文，不要保留原文的断行位置
7. 保留棋谱段落的换行和结构
8. 对不确定的内容，根据将棋上下文合理推断，不要凭空编造
9. 禁止将"飛車"翻译为"战车"；禁止将"手筋"翻译为"手部肌肉"等字面直译
10. 章节标题和小标题使用Markdown加粗格式，如**序章**、**2五桂速攻**
11. **禁止使用HTML或XML标签**（禁止<title>、<text>、<div>等标记），只输出纯文本/Markdown""")
    return "\n".join(lines)
